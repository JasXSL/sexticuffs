		getPronouns(){
			if(!this.pronouns || this.pronouns.length !== 3){
				// Auto generate
				var out = ["he", "him", "his"];
				if(this.hasAnyTag("c_breasts")){
					if(this.hasAnyTag('c_vagina') && this.hasAnyTag('c_penis')){
						out = ["shi", "hir", "hir"];
					}
					out = ["she", "her", "her"];
				}

				return out;
			}
			return this.pronouns;
		}

